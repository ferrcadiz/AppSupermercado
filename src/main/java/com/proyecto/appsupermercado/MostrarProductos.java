    private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
        
        List<Producto> productos = inventario.obtenerTodosLosProductos();
        Producto producto;
        
        productosText.append("Nombre          Categoria          Precio         Stock               Codigo\n");
        
        
        for(int i = 0; i < productos.size(); i++ )
        {
            producto = productos.get(i); 
            
            productosText.append(producto.getNombre()+"          " + producto.getCategoria()+"                   "+producto.getPrecio()+"                      "+producto.getStock()+"                       "+producto.getCodigo()+"\n"+"\n");
        }
    }//GEN-LAST:event_botonMostrarActionPerformed
       
   
    
