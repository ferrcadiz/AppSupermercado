    private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
        List<Proveedor> proveedores = inventario.obtenerTodosLosProveedores();
        Proveedor proveedor;
        
        proveedoresText.append("ID          Nombre          Empresa         Categoria\n");
        //proveedoresText.append("========================================\n");
        
        for(int i = 0; i < proveedores.size(); i++ )
        {
            proveedor = proveedores.get(i); 
            
            proveedoresText.append(proveedor.getId()+"          " + proveedor.getNombre()+"                   "+proveedor.getEmpresa()+"                      "+proveedor.getCategoria()+"\n"+"\n");
        }   
    }//GEN-LAST:event_botonMostrarActionPerformed

